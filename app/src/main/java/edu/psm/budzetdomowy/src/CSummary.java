package edu.psm.budzetdomowy.src;

public class CSummary {
    public String name;
    public float value;

    /**
     * Konstuktor tworzący obiekt CSummary
     * @param name Nazwa podsumowania
     * @param value Wartość podsumownia
     */
    public CSummary(String name, float value) {
        this.name = name;
        this.value = value;
    }
}
